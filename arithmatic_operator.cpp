#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    int a=1,b=2;
    cout<<"value of a+b : "<<a+b<<"\n";
    cout<<"value of a-b : "<<a-b<<"\n";
    cout<<"value of a*b : "<<a*b<<"\n";
    cout<<"value of a/b : "<<a/b<<"\n";
    cout<<"value of a%b : "<<a%b<<"\n";
    cout<<"value of a++ : "<<a++<<"\n";
    cout<<"value of a-- : "<<a--<<"\n";
    cout<<"value of ++a : "<<++a<<"\n";
    cout<<"value of --a : "<<--a<<"\n";
}