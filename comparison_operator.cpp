//following are the type of comparision operators in c++
#include <iostream>

using namespace std;

int main()
{
    cout<<"following are the type of comparision operators in c++ \n";

    int a=5, b=6;
    cout<<a<<"\t";cout<<b<<"\n";
    cout<<"The Value of a == b is "<<(a==b)<<endl; //false '0'
    cout<<"The Value of a != b is "<<(a!=b)<<endl; //true '1'
    cout<<"The Value of a >= b is "<<(a>=b)<<endl;
    cout<<"The Value of a > b is "<<(a>b)<<endl;
    cout<<"The Value of a <= b is "<<(a<=b)<<endl;
    cout<<"The Value of a < b is "<<(a<b)<<endl;




    return 0;
}